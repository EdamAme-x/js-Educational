// 変数応用

const text = "こんにちは！"; // 値を入れられる
// const 変数名 = 値; constは
// text = "おやすみ！"と書き換えられない

let text2 = "おはよう！";
// let 変数名 = 値; letは
text2 = "おやすみ！"; //と書き換えられる

// コードで使える
console.log(text); // こんにちは！

console.log(text2); // おやすみ！

